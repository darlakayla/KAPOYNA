    private void add1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add1MouseClicked
         
        // Validate JTextField
        if (complainant.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your Complainant!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else if(respondent.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the Respondent!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else if(victims.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please type the Victim/s!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else if(accusation.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please type your Accusation!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else if(date.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please type the Date!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }else if(details.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please type the Details!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
       // Validate JComboBox
        if (status.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Please select your Status!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }if (schedule.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please type the Schedule!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        

        
        if(action.equals("Add")){
       
            dbconfiguration dbc = new dbconfiguration();
             int result = dbc.insertData("INSERT INTO tbl_blotterreports (br_complainant, br_respondent, br_victims, br_accusation, br_date, br_details, br_status, br_schedule) "
                + "VALUES ('"+complainant.getText()+"', '"+respondent.getText()+"','"+victims.getText()+"','"+accusation.getText()+"','"+date.getText()+"','"+details.getText()+"','"+status.getSelectedItem()+"','"+schedule.getText()+"')");        
        
        if(result == 1){
            
             close();        
             
                dashBoard db = new dashBoard();              
                blotterReports br = new blotterReports();
                db.maindesktop.add(br).setVisible(true);
                this.dispose();
             
         }else{ 
             System.out.println("Saving Data Failed!");
         }    
        }else if(action.equals("Update")){
            dbconfiguration dbc = new dbconfiguration();
            dbc.updateData("UPDATE tbl_blotterreports "
                + "SET br_complainant = '"+complainant.getText()+"', br_respondent='"+respondent.getText()+"', "
                        + "br_victims ='"+victims.getText()+"', br_accusation='"+accusation.getText()+"',br_date='"+date.getText()+ "',br_details='"+details.getText()+ "',br_status='"+status.getSelectedItem()+ "',br_schedule='"+schedule.getText()+ "'"
                                + "WHERE br_id = '"+id.getText()+"'");
            close();
                dashBoard db = new dashBoard();               
                blotterReports br = new blotterReports();
                db.maindesktop.add(br).setVisible(true);
        }else{
           JOptionPane.showMessageDialog(null, "No Action Selected!");
           close();
        }
    }//GEN-LAST:event_add1MouseClicked

